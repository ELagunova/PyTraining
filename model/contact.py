class Contact:

    def __init__(self, first_name=None, last_name=None, address=None, mobile=None, email=None, bday=None,
                 bmonth=None, byear=None, notes=None):
        self.first_name = first_name
        self.last_name = last_name
        self.address = address
        self.mobile = mobile
        self.email = email
        self.bday = bday
        self.bmonth = bmonth
        self.byear = byear
        self.notes = notes
